def combine_paper_markdown(
    *,
    repo_url: str,
    commit_hash: str,
    section_results: list[dict[str, Any]],
    paper_title: str = "",
    abstract_text: str = "",
    keywords_text: str = "",
    diagram_selections: list[str] | None = None,
) -> str:
    """
    Bolum Writer ciktilarini tek Markdown dosyasinda birlestirir; TRACEABILITY bloklarini sonda toplar.

    Ust kisim: makale basligi + Abstract/Keywords satirlari (em dash); export katmani
    write_markdown_with_ieee_styles icindeki Abstract/Keywords on ek algisiyla stillenir.
    Bos string gelirse DEFAULT_* sablonlari kullanilir.
    """
    title = (paper_title or "").strip() or DEFAULT_COMBINED_PAPER_TITLE
    abst = (abstract_text or "").strip() or DEFAULT_COMBINED_ABSTRACT_PLACEHOLDER
    keys = (keywords_text or "").strip() or DEFAULT_COMBINED_KEYWORDS_PLACEHOLDER

    # IEEE makale govdesinde Repository/Commit satirlari yer almaz; bunlar yalnizca traceability
    # bilgisi olarak ic loglarda kalir. Kullanici istegi: makale govdesi sade tutulsun.
    _ = (repo_url, commit_hash)

    lines: list[str] = [
        f"# {title}",
        f"Abstract\u2014{abst}",
        f"Keywords\u2014{keys}",
        "",
    ]
    trace_chunks: list[str] = []
    all_refs: list[str] = []

    for block in section_results:
        section_heading = str(block.get("section_title") or "Section").strip()
        body = str(block.get("writer_text") or "").strip()
        cleaned_body, section_refs = _extract_references_from_body(body)
        cleaned_body = _strip_traceability_tables(cleaned_body)
        cleaned_body = _strip_inline_references_section(cleaned_body)
        
        # Diyagram placeholder'larını deterministik ekle
        heading_lower = section_heading.lower()
        if any(k in heading_lower for k in ("system", "architecture", "implementation")):
            for diag in (diagram_selections or []):
                cleaned_body += f"\n\n[DIAGRAM:{diag}]"
        
        all_refs.extend(section_refs)
        lines.append(f"## {section_heading}")
        lines.append("")
        lines.append(cleaned_body)
        lines.append("")
        meta = block.get("writer_metadata") or {}
        tr = str(meta.get("traceability") or "").strip()
        if tr:
            trace_chunks.append(f"### {section_heading}\n\n{tr}\n")

    deduped = _dedupe_references(all_refs)
    if deduped:
        lines.append("## References")
        lines.append("")
        for i, ref in enumerate(deduped, start=1):
            lines.append(f"[{i}] {ref}")
        lines.append("")


    if trace_chunks:
        lines.append("---")
        lines.append("")
        lines.append("## TRACEABILITY (all sections)")
        lines.append("")
        lines.extend(trace_chunks)

    return "\n".join(lines).strip()
